<div class="container-fluid">
  <div class="row">
    <div style="font-family: 'roboto',sans-serif" class="col-sm-5"><p>The National Center for Women & Information Technology (NCWIT) is a nonprofit organization that is looking to increase the number of women and non-binary students, of all ages, in computing. NCWIT empowers leaders to recruit, retain and advance women in computing. </p>

 <h3 style = "color: #286BC8" style="font-family: 'roboto',sans-serif">Types of programs and campaigns</h3>
 <p>
NCWIT coordinates a wide variety of programs that have goals in supporting changes in K-12 computing curriculum, empowering women in computing to increase their visibility, working with high school women to encourage them to pursue computing careers, and celebrating the successes of female tech entrepreneurs. </p>

 <h4 style = "color: #286BC8" style="font-family: 'roboto',sans-serif">Aspirations in computing</h4>
<p>The program consists of an award for high school students, undergraduate and graduate students, and provides a community for college women. It engages and encourages women for their computing-relating interests from high school through college and into the workforce. There are a variety of sponsors like: AT&T, Bank of America, Bloomberg, Google, Hewlett-Packard, Intel and much more! </p>

 <h4 style = "color: #286BC8" style="font-family: 'roboto',sans-serif">Pacesetters</h4>
<p>The pacesetters program involves a group of NCWIT member organizations who are committed to an accelerated increase of technical women at their institutions and workplaces. Many leader work across organizational boundaries to improve the participation of women in tech. Setting two year goals is a part of the program as well as releasing diversity data. </p>
